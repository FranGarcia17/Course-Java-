package Bucles;

public class Bucles {
    public static void main(String[] args) {
        // Bucle For
        for(int i = 1; i <= 5; i++) {
            System.out.println("Numero: " + i);
        }

        //Bucle While
        int j = 1;
        while(j <= 5) {
            System.out.println("While: " + j);
            j++;
        }
    }
}
